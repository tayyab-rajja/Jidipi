import React from "react";
import { GetServerSideProps } from "next/types";
import Head from "next/head";

import Layout from "src/components/Layout";
import CardDetails from "src/components/CardDetails/CardDetails";
import CompanyProfile from "src/components/CompanyProfile/CompanyProfile";

import { getPostCategories } from "helpers/changePostsData";
import Sidebar from "src/components/Sidebar";

type Props = {
  post: any;
  sidebarCategories: any;
};

const Post = ({ post, sidebarCategories }: Props) => {
  const categories = getPostCategories(post, "oldCategories");
  const companyImg = post?.companyId?.avatar || null;
  const title = post.title;

  return (
    <div>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
        <link
          href={process.env.NEXT_PUBLIC_SETKA_STYLES_URL}
          rel="stylesheet"
        />
        <script defer src={process.env.NEXT_PUBLIC_SETKA_SCRIPTS_URL}></script>
      </Head>

      <Layout
        SidebarComponent={<Sidebar sidebarCategories={sidebarCategories} />}
      >
        <CardDetails
          categories={categories}
          companyImg={companyImg}
          title={title}
        >
          <div dangerouslySetInnerHTML={{ __html: post.description }} />
        </CardDetails>
        {companyImg && <CompanyProfile companyImg={companyImg} />}
      </Layout>
    </div>
  );
};

export const getServerSideProps: GetServerSideProps = async ({ query }) => {
  const responsePost = await fetch(
    `https://api.jidipi.com/api/v1/post/public/${query.slug}/en`
  );

  const responseSidebarCategories = await fetch(
    "https://api.jidipi.com/api/v1/category?pageFolderId=603ce60958c5c6279bc2ed96"
  );

  const sidebarCategories = await responseSidebarCategories.json();

  const post = await responsePost.json();

  return {
    props: {
      post: post,
      sidebarCategories: sidebarCategories,
    },
  };
};

export default Post;
